package com.apptarixtv.util;

public class ProgramGrid {

	private String channelName;
	private int chanelNo;
	private String channelCategory;
	private String programName;
	private String startTime;
	private String stopTime;
	private String prgDesc;
	private String prgmImgUrl;
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public int getChanelNo() {
		return chanelNo;
	}
	public void setChanelNo(int chanelNo) {
		this.chanelNo = chanelNo;
	}
	public String getChannelCategory() {
		return channelCategory;
	}
	public void setChannelCategory(String channelCategory) {
		this.channelCategory = channelCategory;
	}
	public String getProgramName() {
		return programName;
	}
	public void setProgramName(String programName) {
		this.programName = programName;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getStopTime() {
		return stopTime;
	}
	public void setStopTime(String stopTime) {
		this.stopTime = stopTime;
	}
	public String getPrgDesc() {
		return prgDesc;
	}
	public void setPrgDesc(String prgDesc) {
		this.prgDesc = prgDesc;
	}
	public String getPrgmImgUrl() {
		return prgmImgUrl;
	}
	public void setPrgmImgUrl(String prgmImgUrl) {
		this.prgmImgUrl = prgmImgUrl;
	}
	
}
